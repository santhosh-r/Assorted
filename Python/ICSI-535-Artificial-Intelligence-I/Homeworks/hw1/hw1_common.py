def check_symmetry(a):
  n = len(a[0])
  for i in range(n):
    for j in range(n):
      if a[i, j] != a[j, i]:
        return False
  return True

def get_path(parents, end, start):
  if start not in parents.keys() or end not in parents.keys():
    return None
  path = [end]
  while path[0] != start:
    path = [parents[path[0]]] + path
  return path

def search_am(method, graph, start, goal, visited):
  [nodes, adjmat] = graph
  nodes_keys = list(nodes.keys())
  if start not in nodes_keys or goal not in nodes_keys:
    return None
  start, goal = nodes[start], nodes[goal]
  v = []
  r = method(adjmat, start, goal, v)
  for i in v:
    visited.append(nodes_keys[i])
  if r is not None:
    r = [nodes_keys[i] for i in r]
  return r

